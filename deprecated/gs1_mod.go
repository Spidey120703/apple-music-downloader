func GetContentProvider(upc string) string {
	for contentProviderName, licenceKeys := range ContentProviderMapping {
		for _, licenceKey := range licenceKeys {
			trimedUPC := strings.TrimLeft(upc, "0")
			trimedKey := strings.TrimLeft(licenceKey, "0")
			if strings.HasPrefix(trimedUPC, trimedKey) {
				return contentProviderName
			}
		}
	}
	return ""
}

