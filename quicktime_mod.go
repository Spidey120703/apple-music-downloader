var GenreMapped = make(map[string]int)

func init() {
	for genreID, genreName := range QuickTimeGenreMapping {
		GenreMapped[genreName] = genreID

		aliases, ok := CNGenreMapping[genreName]
		if !ok {
			continue
		}
		for _, alias := range aliases {
			GenreMapped[alias] = genreID
		}
	}
}

func GetQTGenreID(genreNames []string) int {
	if len(genreNames) == 0 {
		return 0
	}
	return GenreMapped[genreNames[0]]
}

